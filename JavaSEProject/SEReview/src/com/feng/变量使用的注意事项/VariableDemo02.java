package com.feng.变量使用的注意事项;

public class VariableDemo02 {
    public static void main(String[] args) {
        //定义byte类型的变量
        byte b= 10;
        System.out.println(b);

        //定义short类型的变量
        short s= 10;
        System.out.println(s);

        //定义int类型的变量
        int i= 10;
        System.out.println(i);

        //定义double类型的变量
        double d= 10;
        System.out.println(d);

        //定义char类型的变量
        char c= 'a';
        System.out.println(c);

        //定义boolean类型的变量
        boolean bb = true;
        System.out.println(bb);

        //定义long类型
        long l =1000000L;
        System.out.println(l);
        System.out.println("------");

        //定义float类型的变量
        float f =13.14F;
        System.out.println(f);
    }
}
